def download_events():
    os.makedirs(OUT_DIR, exist_ok=True)
    for name, gps in EVENTS:
        start = gps - DURATION/2
        end   = gps + DURATION/2
        for det in DETECTORS:
            fname = f"{name}_{det}.npy"
            outpath = os.path.join(OUT_DIR, fname)
            if os.path.exists(outpath):
                print(f"跳过已存在: {fname}")
                continue
            print(f"下载 {name} {det} ...")
            try:
                ts = TimeSeries.fetch_open_data(det, start, end,
                                                sample_rate=SAMPLE_RATE,
                                                verbose=False, timeout=60)
                if len(ts) == 0:
                    print(f"  -> 无数据，跳过")
                    continue
                np.save(outpath, ts.value)
                print(f"  -> 保存至 {outpath} (shape={ts.shape})")
            except Exception as e:
                print(f"  -> 失败: {e}")

if __name__ == "__main__":
    download_events()
